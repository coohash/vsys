import os
import csv
import json
import subprocess
import sys

# ====================================================================================
# ⚙️ 【系统架构核心配置区 / SYSTEM CONFIGURATION MATRIX】
# ====================================================================================
# 提示：本区域定义了分发系统的全部输入/输出（I/O）数据流水文件。
# 请确保脚本运行目录具备完整的读写权限，且避免使用 Excel 独占打开以下任意文件以防 I/O 锁死。
# ------------------------------------------------------------------------------------

# [1. 过程账本日志] 记录子模块执行转账时的链上原始哈希、广播状态与区块高度，用于后期审计与对账。
LOG_FILE = "transfer_log.csv"

# [2. 核心状态机状态文件] 存储由 gen_ledger.py 生成的复杂层级内循环资产拓扑账本，下游分发引擎基于此执行。
LEDGER_FILE = "task_ledger.json"

# [3. 缓冲批处理名单] 从总名单提取出来的“当前批次”接收钱包及对应金额，将作为输入参数直接喂给子脚本。
RECIPIENTS_FILE = "recipients.csv"

# [4. 永续历史归档库] 已经被成功提取、处理完毕的历史接收地址与金额会自动追加（Append）存储到此，永不覆盖。
COMPLETE_FILE = "complete-send.csv"

# [5. 核心动态待发总库] 存放海量尚未分发的原始地址与金额清单。系统每提取 N 条，该文件就会物理裁剪删除 N 条。
SENDLIST_FILE = "sendlist.csv"


def run_command(cmd):
    """
    【核心进程调用机制】
    通过当前系统的 Python 解释器安全、阻塞式地派生（Spawn）子进程，运行外部功能模块。
    :param cmd: 字符串类型，指向目标 Python 脚本名称（如 "gen_ledger.py"）
    :return: CompletedProcess 实例，包含子进程的退出状态码（returncode）与执行断言
    """
    return subprocess.run([sys.executable, cmd])


def main():
    print(f"\n{'='*50}")
    print(f"🛰️  VSYS 资金分发自动化系统启动")
    print(f"{'='*50}\n")

    # --------------------------------------------------------------------------------
    # --- 阶段 1. 重置运行时环境：清理旧任务数据 ---
    # --------------------------------------------------------------------------------
    # 防止上一次因不可抗力中断、残留的过期账本或日志干扰本次全新批次的资产计算精度。
    for f in [LOG_FILE, LEDGER_FILE]:
        if os.path.exists(f):
            os.remove(f)
            print(f"🗑️  已清理旧文件: {f}")

    # --------------------------------------------------------------------------------
    # --- 阶段 2. 状态原子转移：归档已完成地址 ---
    # --------------------------------------------------------------------------------
    # 财务对账保护机制：检查 recipients.csv 是否有上批次未归档完的陈旧残留。
    # 若有，将其完整追加到 complete-send.csv 历史大库中，随后将 recipients.csv 彻底截断置空。
    if os.path.exists(RECIPIENTS_FILE):
        with open(RECIPIENTS_FILE, 'r', encoding='utf-8-sig') as f:
            old_recipients = list(csv.reader(f))
        
        if old_recipients:
            with open(COMPLETE_FILE, 'a', newline='', encoding='utf-8-sig') as f:
                writer = csv.writer(f)
                writer.writerows(old_recipients)
            print(f"📦 已将旧地址归档至: {COMPLETE_FILE}")
        
        # 物理重置 recipients.csv，确保接下来写入全新的切片数据时，文件处于干净的状态
        open(RECIPIENTS_FILE, 'w').close()
        print(f"🧹 已清空: {RECIPIENTS_FILE}")

    # --------------------------------------------------------------------------------
    # --- 阶段 3 & 4. 深度切片与流量截断：从待发送名单中提取地址 ---
    # --------------------------------------------------------------------------------
    # 运行红线拦截：如果本地目录缺失最基础的待发总库 sendlist.csv，脚本立即终止，保护系统不触发空指针。
    if not os.path.exists(SENDLIST_FILE):
        print(f"❌ 错误: 找不到待发送名单 {SENDLIST_FILE}")
        return

    # 人机交互控制点：输入本次需要从总库中“切片剥离”出来的精准钱包数量 N
    try:
        n_input = input("🔢 请输入本次要处理的地址数量 (N): ")
        n = int(n_input)
    except ValueError:
        print("❌ 错误: 请输入有效的数字")
        return

    # 读取包含签名策略或待发资产的总池
    with open(SENDLIST_FILE, 'r', encoding='utf-8-sig') as f:
        all_sendlist = list(csv.reader(f))

    if not all_sendlist:
        print("⚠️  警告: sendlist.csv 为空")
        return

    # 【深度裁剪矩阵算法】
    # 提取前 N 条作为当前处理序列 -> to_process
    # 提取第 N 条之后直至末尾的所有数据作为幸存序列 -> remaining
    to_process = all_sendlist[:n]
    remaining = all_sendlist[n:]

    # 将剥离出来的 N 条当前批次地址，安全落盘至临时的缓冲批处理名单，作为下游子脚本的直接入参
    with open(RECIPIENTS_FILE, 'w', newline='', encoding='utf-8-sig') as f:
        writer = csv.writer(f)
        writer.writerows(to_process)
    print(f"📥 已从待发送名单提取 {len(to_process)} 条地址")

    # 【财务防重漏核心保护】将剥离删除后的幸存序列覆盖写入 sendlist.csv，实现“发 N 个，少 N 个”，
    # 从物理原理上绝对杜绝了同一批地址在后续长效运行中被重复调用分发的可能！
    with open(SENDLIST_FILE, 'w', newline='', encoding='utf-8-sig') as f:
        writer = csv.writer(f)
        writer.writerows(remaining)
    print(f"📝 待发送名单已更新，剩余 {len(remaining)} 条")

    # --------------------------------------------------------------------------------
    # --- 阶段 5. 层级解耦与调度：调用 gen_ledger.py ---
    # --------------------------------------------------------------------------------
    # 调用账本生成脚本。该子脚本会读取刚刚落盘的 recipients.csv，在内存中动态推演、
    # 匹配中转大户地址，计算矿工费，并将最终生成的分布式资产拓扑树定型存储到 task_ledger.json。
    print("\n⚙️  正在生成账本拓扑...")
    gen_res = run_command("gen_ledger.py")
    
    # 状态阻断断言：如果 gen_ledger.py 内部校验失败（例如私钥不匹配、余额不足等）且抛出了非 0 状态码，
    # 本主控系统将立即切断核心执行引擎的启动，防止产生盲目上链导致资产永久丢失。
    if gen_res.returncode != 0:
        print("❌ 账本生成失败，请检查脚本。")
        return

    # --------------------------------------------------------------------------------
    # --- 阶段 6. 最终风控确认与点火启动 ---
    # --------------------------------------------------------------------------------
    # 此时 gen_ledger.py 已经将财务报表（如总计所需 VSYS 数量、独立地址规模）打印在终端最上方。
    # 运维架构师必须进行最后一次人工核对，输入 Y/N 来决定是否开始链上广播交易。
    confirm = input("\n⚠️  请核对上方财务报告。确认开始发送吗？(Y/N): ").strip().upper()
    
    if confirm == 'Y':
        print("\n🚀 正在启动执行引擎...\n")
        # 验证成功，正式点火！派生终极执行引擎 vsys_untraceable_mixer.py，
        # 该引擎会异步解析 task_ledger.json 并以最高并发速率向 VSYS 节点高速广播交易字节流。
        run_command("vsys_untraceable_mixer.py")
    else:
        # 如果按下 N 取消，系统将拒绝广播，但已生成的精细层级拓扑账本仍然会完整保留在 task_ledger.json 中。
        print("\n✋ 操作已取消。账本已保留在 task_ledger.json 中。")

if __name__ == "__main__":
    main()